# PART 2: ELBOW METHOD (Finding the optimal k)
# ==========================================
print("--- RUNNING ELBOW METHOD ---")
inertia = []
K_range = range(1, 11)

for k in K_range:
    # Explicitly setting n_init=10 prevents annoying FutureWarnings in newer sklearn versions
    kmeans = KMeans(n_clusters=k, random_state=42, n_init=10)
    inertia.append(kmeans.fit(X_2d).inertia_)

# Plot the Elbow Graph
plt.figure(figsize=(8, 5))
plt.plot(K_range, inertia, marker='o')
plt.title('Elbow Method to Find Optimal k')
plt.xlabel('Number of Clusters (k)')
plt.ylabel('Inertia (Sum of Squared Distances)')
plt.grid(True)
plt.show()

# ==========================================
# PART 3: VISUALIZE CLUSTERS
# ==========================================
print("\n--- VISUALIZING CLUSTERS (k=2, k=3, k=5) ---")
# We test 2, 3 (which the elbow method shows is optimal), and 5.
for k in [2, 3, 5]:
    kmeans = KMeans(n_clusters=k, random_state=42, n_init=10)
    clusters = kmeans.fit_predict(X_2d)

    plt.figure(figsize=(8, 5))
