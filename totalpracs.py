import numpy as np
import matplotlib.pyplot as plt
from sklearn.preprocessing import PolynomialFeatures
from sklearn.linear_model import LinearRegression
from sklearn.metrics import mean_squared_error

# ==========================================
# PART 1: Polynomial Regression using Sklearn
# (In-sample vs Out-sample errors)
# ==========================================
np.random.seed(0)

# Number of training points
N = 15

# Generate x values
x = np.random.uniform(-1, 1, N)
x = x.reshape(-1, 1)

# True polynomial coefficients (10th order)
coef = np.random.randn(11)

# True function f(x)
def f(x):
    y = 0
    for i in range(11):
        y += coef[i] * (x ** i)
    return y

# Generate noisy observations
y = f(x) + np.random.normal(0, 1, N).reshape(-1, 1)

# Test points for plotting
xtest = np.linspace(-1, 1, 400).reshape(-1, 1)

# Plot training data
plt.figure(figsize=(8, 5))
plt.scatter(x.flatten(), y.flatten(), label="Training data")

# Fit models of degree 2 and 10
for d in [2, 10]:
    poly = PolynomialFeatures(d)
    X_poly = poly.fit_transform(x)

    model = LinearRegression()
    model.fit(X_poly, y)

    # In-sample error (Ein)
    y_pred = model.predict(X_poly)
    Ein = mean_squared_error(y, y_pred)

    # Out-sample error (Eout)
    xt = np.random.uniform(-1, 1, 1000).reshape(-1, 1)
    Xt_poly = poly.transform(xt)
    y_true = f(xt)
    y_out = model.predict(Xt_poly)
    Eout = mean_squared_error(y_true, y_out)

    print(f"Degree {d} | Ein= {Ein:.4f} | Eout = {Eout:.4f}")

    # Plot model curve
    Xtest_poly = poly.transform(xtest)
    y_plot = model.predict(Xtest_poly)
    plt.plot(xtest, y_plot, label=f"Model degree({d})")

# Plot true function
plt.plot(xtest, f(xtest), label="True function f(x)")
plt.xlabel("x")
plt.ylabel("y")
plt.title("Polynomial Regression: Underfitting vs Overfitting")
plt.legend()
plt.ylim(-10, 10) # Set limit to visualize nicely
plt.show()


