import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
from sklearn.datasets import load_wine
from sklearn.preprocessing import StandardScaler
from sklearn.decomposition import PCA

# ==========================================
# PART 1: LOAD & SCALE DATA
# ==========================================
print("--- LOADING AND SCALING DATA ---")
wine = load_wine()
X, y, features = wine.data, wine.target, wine.feature_names

# Standardization is absolutely mandatory for PCA so that features
# with larger scales (like 'proline') don't dominate the components.
scaler = StandardScaler()
X_scaled = scaler.fit_transform(X)

# ==========================================
# PART 2: APPLY PCA
# ==========================================
# Apply PCA keeping all components first to calculate variance
pca = PCA()
X_pca = pca.fit_transform(X_scaled)

# Calculate the cumulative explained variance
cumulative_variance = np.cumsum(pca.explained_variance_ratio_)

# ==========================================
# PART 3: SCREE PLOT
# ==========================================
print("--- PLOTTING SCREE PLOT ---")
plt.figure(figsize=(8, 5))
plt.plot(cumulative_variance, marker='o')
plt.title("Scree Plot (Cumulative Variance)")
plt.xlabel("Number of Components")
plt.ylabel("Cumulative Explained Variance")
plt.grid(True)
plt.show()

# ==========================================
# PART 4: 2D PROJECTION PLOT
# ==========================================
print("\n--- PLOTTING 2D PCA PROJECTION ---")
# To plot in 2D, we just slice the first two columns of the PCA result
plt.figure(figsize=(8, 5))
scatter = plt.scatter(X_pca[:, 0], X_pca[:, 1], c=y, cmap='viridis')
plt.title("PCA Projection (2D)")
plt.xlabel("Principal Component 1")
plt.ylabel("Principal Component 2")
plt.legend(*scatter.legend_elements(), title="Classes")
plt.grid(True)
plt.show()

# ==========================================
# PART 5: FEATURE IMPORTANCE & VARIANCE RETAINED
# ==========================================
# pca.components_ contains the eigenvectors. We just slice the first 2 rows.
components_df = pd.DataFrame(pca.components_[:2, :], columns=features, index=["PC1", "PC2"])

print("\n--- PRINCIPAL COMPONENT WEIGHTS (First 2 PCs) ---")
print(components_df.T) # Transposed for easier reading in the console

# Print how much of the original data's variance is retained by just 2 components
print(f"\nVariance Retained by 2 Components: {cumulative_variance[1]:.4f}")

